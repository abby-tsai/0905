				很長一<b>段時間，我不曾細細</b>地觸摸我們愛情的紋理。<br />
				<i>工作、小孩、家事、讀書、父母</i>，細碎尋<u>常的瑣事佔據了心思的全部，</u>以至於，我天天見你，卻，不常想起我們。<br />
                <br />
				<h2>前幾天，在臉書上，我發現一個年輕的攝影師。</h2>他為太太拍的其中幾張相片，讓我想起你在戀愛的那段時間，拍下來的我，有著一種愛人眼裡才看得到的美好。照片裡的女人，遠遠地回過頭望，微笑，很寧靜的氛圍。她在結婚後的第九個年頭，離開了這個世界。讀著他書寫的那些想念、愛、回憶，我的心在夜裡，安靜地疼痛。<br />
                死亡，讓失去和擁有同時變得鮮明。<br />
                <br />
				<h3>我注視著我們的情感。</h3><br />
                試著弄懂，我是如何走了七個年頭到這裡？何以我仍然能夠感受到因為你在，我可以相信，自己做得到別人眼裡看來不可能的事情；我可以相信，日子裡難熬、衝突和那些令人失望的時刻，總是會過去。<br />
                你呢？你又是怎麼看待這兩千五百多日的時光？是什麼讓你直到現在，還能常常感受到我給的照顧和瞭解，覺得滿足；讓你直到現在，依舊有能力給自己夢想和動力，要把我們帶往這兒那兒。<br />
                我們是怎麼找到彼此的？<br />
                <br />
				<h4>今天下午，很久沒有一起窩在書房裡工作的我跟你，</h4>難得地並肩坐在那張大書桌前。沒多久，非常少見地，你坐到地上，開始整理起書櫃裡頭的雜物，翻出了好久沒用的老數位相機和充電線、翻出了兒子出生前我們去台南為他親手做的牛皮書衣......，你陷進回憶裡，感受著我們這個家......。<br />
                側頭看著你，我沒有說話，想起許多最初的往事。<br />
                你領著我熟悉那台老底片相機和攝影的世界，讓我的耳朵聽見陳建年海洋的聲音、Beyond激昂的情懷......；你閱讀我的文字、感受我內在那些蜿蜒起伏的情感，你聽我談性別和政治的觀點、與我對話，你好奇我那些一個人的旅行、從觀者變為我的旅伴......。<br />
                然後我意識到，重要的不只是我們相遇。重要的是，我們願意花時間停留在自己和對方的身上，於是，我們才沒有只是擦身而過。<br />
                <br />
                找尋伴侶的男人常常在說，遇到一個好對象很難。<br />
                去哪裡遇見一個願意了解跟接受自己、與自己一起生活、同起同落同患難的伴。有些男人可能都不願意再浪費光陰，去認識也許不適合自己的人；有些男人，說不定也沒認真想過自己到底需要一個怎樣的伴，反正外型符合、背景條件相近，最重要的是，對未來的想像也差不多，其他的，再慢慢改變對方就好。<br />
                <br />
                而，男人們怎麼會覺得這麼速成的找尋，牽起的會是幸福呢？<br />
                <br />
                <ul>
					<li>你總是需要花時間認識你自己。</li>
					<li>你喜歡在生活裡做些什麼？你想要什麼樣的家？</li>
					<li>你都是怎麼愛人的？你需要被愛的方式是什麼？</li>
					<li>你不完美而需要被包容的是什麼？</li>
					<li>在關係中衝突或受傷的時候，你需要的是什麼？</li>
				</ul>
                <br />
                這些問題的答案，應該讓你能更安穩而真實，了解自己需要什麼，也幫助想靠近你的人讀懂你。這些問題的答案，卻不必然成為一張檢核清單，用來篩選過濾一個個可能的對象，也不需要成為一張標準藍圖，用來把別人改建成符合你需要的樣子，因為對方不是一個物品，她/他也是她/他自己的樣貌。<br />
                <br />
                <ol>
					<li>所以，你也總是需要花時間認識你找到的這個人。</li>
					<li>她/他的夢想是什麼？她/他是怎麼建構家的圖像的？</li>
					<li>她/他在愛情裡想要怎麼被對待？</li>
					<li>她/他內心深處害怕自己不會被接受的是什麼？</li>
				</ol>
                在跟你吵架或她/他自己感到脆弱的時候，什麼會讓她/他重新恢復安全感？<br />
                <br />
                這些問題的答案，應該讓你能更完整地認識她/他，光亮的那一面...和晦暗的那一面...，有力量的那一面...和脆弱的那一面...，豐富的那一面...和匱乏的那一面...，能滋養你的那一面...和會吞噬你的那一面......。一個人真實的全部，必然包含這些面貌。<br />
                <br />
                而後，你需要花時間感受你們。<br />
                在兩個人一來一往之間，你們會感受到自己是不是能夠被對方更擴展，對方是不是能夠在做自己與滿足你之間找到平衡，彷若舞蹈一般，你們會知道一進一退的節奏，是不是，越來越合拍了。<br />
                <br />
                誰能給你幸福，這個複雜的問題，不會有簡單的答案，也不會有快速的過程。<br />
                如果講求速度與簡單，那麼，賭上的只是自己的運氣。<br />
                <br />
                能走得長遠而幸福的親密關係，美好的地方，就在於我們會花時間認識一個真實的人，也能做一個真實的自己。也許有些地方不被喜歡、有些事情會被生氣、有些時候會被責備，但總的來說，你會感受到有一個人，她/他覺得你不完美，但是，她/他仍然愛你。<br />
                那麼，生命面對很多恐懼，我們才有勇氣；日子裡遇到的那些辛苦，我們才有撐下去的意義。因為，有人見證和分享，同行和扶持。<br />
                <br />
                花時間，我們，才遇到了幸福。
