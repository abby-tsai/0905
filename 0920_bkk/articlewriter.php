		$(window).load(function() {
			var Gww = $(window).width(),
				$articlePad = $('.articleMenu'),
				$artMenu = $('.articleMenu > ul'),
				$artMenuList = $artMenu.find('li'),
				$artMenuLi_w = $artMenuList.width(),
				$artMenu_w = ($artMenuList.width()) * $artMenuList.length + 40,
				$limit = $artMenu_w - Gww;
				
			if ($limit > 0) {
				$articlePad.addClass('Left');
				$artMenu.css({'width':$artMenu_w});
				$('.articleMenu nav').click(function(){article('Left');});
				$artMenu.touchwipe({
					wipeLeft: function() {article('Left')},
					wipeRight: function() {article('')},
					preventDefaultEvents: true							
				});	
			}
			function article(distan) {
				var $now = parseInt($artMenu.css('margin-left'));
				if (distan === 'Left' && $now >= ( 0 - $limit)) {
					$artMenu.stop().animate({marginLeft: $now - $artMenuLi_w});
				} else if ($now < 0){
					$artMenu.stop().animate({marginLeft: 0});
				}
				
			}
		});		
